def update_file(file_name, new_content):
    file_path = f"lib/app/core/translations/{file_name}.dart"
    if not os.path.exists(file_path):
        print(f"File {file_path} not found.")
        return
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
        
    if "'keep_board_title'" in content:
        print(f"{file_name} already contains keep translations.")
        return
        
    idx = content.rfind('};')
    if idx != -1:
        updated = content[:idx] + new_content + "};\n"
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(updated)
        print(f"Successfully appended {file_name}")
    else:
        print(f"Failed to find }}; in {file_name}")

for lang, text in translations.items():
    update_file(lang, text)
